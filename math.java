 public class MathFunctions {
 public static void main(String[] args) {
 double num1 = 16.0;
 double num2 = 8.5;
 System.out.println("Square root of " + num1 + ": " + Math.sqrt(num1));
 System.out.println("Absolute value of-8.5: " + Math.abs(-8.5));
 System.out.println("Maximum between " + num1 + " and " + num2 + ": " +
 Math.max(num1, num2));
 System.out.println("Minimum between " + num1 + " and " + num2 + ": " +
 Math.min(num1, num2));
 System.out.println("Ceiling of " + num2 + ": " + Math.ceil(num2));
 System.out.println("Floor of " + num2 + ": " + Math.floor(num2));
 }
 }
